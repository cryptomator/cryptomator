package org.cryptomator.ui.vaultoptions;

public enum SelectedVaultOptionsTab {
	/**
	 * Let the controller decide which tab to show.
	 */
	ANY,

	/**
	 * Show general tab
	 */
	GENERAL,

	/**
	 * Show mounting tab
	 */
	MOUNT,

	/**
	 * Show password tab
	 */
	KEY,

	/**
	 * Show Auto-Lock tab
	 */
	AUTOLOCK,

	/**
	 * Show health tab
	 */
	HEALTH;
}
