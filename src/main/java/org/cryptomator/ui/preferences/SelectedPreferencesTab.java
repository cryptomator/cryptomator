package org.cryptomator.ui.preferences;

public enum SelectedPreferencesTab {
	/**
	 * Let the controller decide which tab to show.
	 */
	ANY,

	/**
	 * Show general tab
	 */
	GENERAL,

	/**
	 * Show volume tab
	 */
	VOLUME,

	/**
	 * Show updates tab
	 */
	UPDATES,

	/**
	 * Show contribute tab
	 */
	CONTRIBUTE,

	/**
	 * Show about tab
	 */
	ABOUT,
}
